bool at = false;
void lt(int speed){
  motor[port2] = speed;
  motor[port4] = -speed;
  int ltat = speed;
}
void rt(int speed){
  motor[port3] = speed;
  motor[port5] = -speed;
  int rtat = speed;
}
task autonomous(){
	lt(100);
	rt(100);
	wait1Msec(3000);
	lt(0);
	rt(0);
}
task usercontrol(){
	int rtat=0;
	int ltat=0;
  while (1 == 1){
    if (vexRT[Ch2] == 0){//point turns, positive is clockwise
      lt(vexRT[Ch1]);//east||north
      rt(-vexRT[Ch1]);//west||south
    }else {//differential
      lt(vexRT[Ch2] + vexRT[Ch1]);//north + east
      rt(vexRT[Ch2] - vexRT[Ch1]);//north - east
    }
    if ( vexRT[Btn6U] == 1 ) {
    	bool at = true;
    }
    if ( vexRT[Btn6D] == 1 ) {
    	bool at = false;
    }
    if (at != true){
	    if (vexRT[Btn5U] == 1){
 	    motor[port8] = -127;
    	}else if (vexRT[Btn5D] == 1){
      	motor[port8] = 127;
    	}else {
      	motor[port8] = 0;
    	}
    }
    if ( at == true && rtat >= 100 && ltat >= 100){
    	motor[port8] = -127;
    }
    if ( at == true && rtat <= -100 && ltat <= -100){
    	motor[port8] = 127;
    }
  }
}
